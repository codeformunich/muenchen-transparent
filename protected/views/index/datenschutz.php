<h1>Datenschutzerklärung</h1>

<h2>Protokollierung von Seitenaufrufen</h2>
<p>Aufrufe dieser Seite werden vom Server des Ratsinformanten mitprotokolliert. In diesem Protokoll wird die IP-Adresse und der Zeitpunkt des Aufrufs, die aufgerufene Seite sowie
	die Information, ob dieser Aufruf erfolgreich war gespeichert. Es ist uns nicht möglich, diese Daten einer bestimmten natürlichen Person zuzuordnen.<br>
	Diese Angaben werden von uns ausschließlich zu statistischen Zwecken ausgewertet. Die Daten werden weder an Dritte weitergegeben noch mit anderen Daten verknüpft und werden
	nach einer Woche gelöscht.</p>

<h2>E-Mail-Benachrichtigungen</h2>
<p>Wir bieten die Möglichkeit, sich per E-Mail über neue Anträge, Dokumente oder andere Ereignisse informieren zu lassen. Dazu benötigen wir neben Ihrer E-Mail-Adresse die
	Bestätigung, dass Sie der Inhaber der angegebenen Email-Adresse sind und mit dem Empfang der Benachrichtigungen einverstanden sind. Dies überprüfen wir, indem bei der ersten
	eingerichteten Benachrichtigung eine Bestätigungs-E-Mail an die angegebene Adresse geschickt wird. Sie müssen den darin angegebenen Link anklicken oder auf der Website den in
	der E-Mail angebenenen Bestätigungscode eingeben. Der Bestätigungscode ist gleichzeitig das anfängliche Passwort, das verwendet werden kann, um weitere Benachrichtigungen
	hinzufügen bzw. wieder entfernen zu können. Es kann auch nachträglich geändert werden.</p>
<p>
	Die E-Mail-Adresse wird nur erhoben, um Ihnen die Benachrichtigungen zuschicken zu können. Um unsere Berechtigung zum Versand der Benachrichtigung dokumentieren zu können, wird
	außerdem der Zeitpunkt und die IP-Adresse der Bestätigung des Erhalts besagter ersten E-Mail gespeichert. Eine Weitergabe der Daten an Dritte erfolgt nicht. Die Bestellung des
	Newsletters und Ihre Einwilligung zur Speicherung der E-Mail-Adresse können Sie jederzeit widerrufen, beispielsweise duch Aufruf des "Abbestellen"-Links in jeder
	E-Mail-Benachrichtigung oder einfach per E-Mail an die im Impressum angegebene E-Mail-Adresse.
</p>

<h2>Einsatz von Cookies</h2>
<p>
	Cookies sind Datensätze, die vom Webserver an den Browser geschickt und dort für einen späteren Abruf gespeichert werden. Ein Cookie wird gesetzt, wenn Sie die
	E-Mail-Benachrichtigungsfunktion nutzen, um die verwendete E-Mail-Adresse zwischenzuspeichern (damit Sie nicht bei jedem Hinzufügen / Entfernen einer Benachrichtigung die
	E-Mail-Adresse und das verbundene Passwort erneut eingeben müssen). Beim Schließen des Browsers wird das Cookie wieder gelöscht.
</p>

<h2>Datensicherheit</h2>
<p>Der Datenverkehr zwischen Ihrem Browser und unserem Server wird grundsätzlich per HTTPS verschlüsselt und ist damit weit gehend vor dem Abhören dazwischen und
	Man-In-The-Middle-Angriffen sicher.<br>
	Das vergebene Passwort, um neue Benachrichtigungen zu einer E-Mail-Adresse hinzufügen oder entfernen zu können, wird nur in Form eines One-Way-Hashes gespeichert, sodass es
	nicht möglich ist, daraus das ursprüngliche Passwort zu rekonstruieren.<br>
	Die Server stehen innerhalb der Europäischen Union.
</p>

